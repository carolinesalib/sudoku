require 'spec_helper'
require_relative '../lib/sudoku'

describe Sudoku do
  let(:valid_matrix) do
    [
      [5, 3, 4, 6, 7, 8, 9, 1, 2],
      [6, 7, 2, 1, 9, 5, 3, 4, 8],
      [1, 9, 8, 3, 4, 2, 5, 6, 7],
      [8, 5, 9, 7, 6, 1, 4, 2, 3],
      [4, 2, 6, 8, 5, 3, 7, 9, 1],
      [7, 1, 3, 9, 2, 4, 8, 5, 6],
      [9, 6, 1, 5, 3, 7, 2, 8, 4],
      [2, 8, 7, 4, 1, 9, 6, 3, 5],
      [3, 4, 5, 2, 8, 6, 1, 7, 9]
    ]
  end

  let(:invalid_matrix) do
    [
      [5, 3, 4, 6, 7, 8, 9, 1, 2],
      [6, 7, 2, 1, 9, 5, 3, 4, 8],
      [1, 9, 8, 3, 4, 2, 5, 6, 7],
      [8, 5, 9, 7, 6, 1, 4, 2, 3],
      [4, 2, 6, 8, 5, 3, 7, 9, 1],
      [7, 1, 3, 9, 2, 4, 8, 5, 6],
      [9, 6, 1, 5, 3, 7, 2, 8, 4],
      [2, 8, 7, 4, 1, 9, 6, 3, 5],
      [3, 4, 5, 2, 8, 6, 9, 7, 9]
    ]
  end

  let(:invalid_matrix_2) do
    [
      [5, 3, 4, 6, 7, 8, 9, 1, 2],
      [6, 7, 2, 1, 9, 5, 3, 4, 8],
      [1, 9, 8, 3, 4, 2, 5, 6, 7],
      [8, 5, 9, 7, 6, 1, 4, 2, 3],
      [4, 2, 6, 8, 5, 3, 7, 9, 1],
      [7, 1, 3, 9, 2, 4, 8, 5, 6],
      [9, 6, 1, 5, 3, 7, 2, 8, 4],
      [2, 8, 7, 4, 1, 5, 6, 3, 9],
      [3, 4, 5, 2, 8, 6, 1, 7, 9]
    ]
  end

  describe '.valid?' do
    context 'when sudoku matrix is valid' do
      it 'returns true' do
        expect(described_class.valid?(valid_matrix)).to eq(true)
      end
    end

    context 'when sudoku matrix has invalid line' do
      it 'returns false' do
        expect(described_class.valid?(invalid_matrix)).to eq(false)
      end
    end

    context 'when sudoku matrix has invalid column' do
      it 'returns false' do
        expect(described_class.valid?(invalid_matrix_2)).to eq(false)
      end
    end
  end
end
